You define a local variable inside of a method. local variables are just like global variables but are called upon inside of a method. They should be defined first by giving the local variable a name. The name should be lowercase. Then the "=" symbol and lastly, what the variable will contain.

How do you define a method?

You define a method by first typing "def".
Next is the methods name. The methods name should be lower case.
on the next line you write your code for what the method should do and lastly you type "end" to close the method.

What is the difference between a local variable and a method?

A local variable is a variable that is declared within a method. A method uses code and sometimes local variables to output some kind of result.

